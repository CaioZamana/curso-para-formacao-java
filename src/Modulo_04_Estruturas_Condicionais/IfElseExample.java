package Modulo_04_Estruturas_Condicionais;

public class IfElseExample {
    public static void main(String[] args) {
        int numero = 10;

        if (numero > 0) {
            System.out.println("O número é positivo.");
        } else {
            System.out.println("O número é zero ou negativo.");
        }
    }
}
