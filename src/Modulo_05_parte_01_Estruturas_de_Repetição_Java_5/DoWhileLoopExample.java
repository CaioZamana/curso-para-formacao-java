package Modulo_05_parte_01_Estruturas_de_Repetição_Java_5;

public class DoWhileLoopExample {
    public static void main(String[] args) {
        int i = 0;

        do {
            // Código a ser repetido
            System.out.println("Valor de i: " + i);
            i++;
        } while (i < 5);
    }
}
